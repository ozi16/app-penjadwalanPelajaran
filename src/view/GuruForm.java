    private void tableGuruMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableGuruMouseClicked
        int baris = tableGuru.getSelectedRow();

    txtId.setText(tableGuru.getValueAt(baris, 0).toString());
    txtNip.setText(tableGuru.getValueAt(baris, 1).toString());
    txtNamaGuru.setText(tableGuru.getValueAt(baris, 2).toString());

    String jk = tableGuru.getValueAt(baris, 3).toString();
    cbJenisKelamin.setSelectedItem(jk);
    }//GEN-LAST:event_tableGuruMouseClicked

    private void resetForm() {
    txtId.setText("");
    txtNip.setText("");
    txtNamaGuru.setText("");
    cbJenisKelamin.setSelectedIndex(0);
}

