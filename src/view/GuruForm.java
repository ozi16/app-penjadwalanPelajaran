    private void tampilDataGuru() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID");
    model.addColumn("NIP");
    model.addColumn("Nama Guru");
    model.addColumn("Jenis Kelamin");

    try {
        Connection conn = DatabaseConnection.getConnection();
        Statement stmt = conn.createStatement();
        ResultSet rs = stmt.executeQuery("SELECT * FROM guru");

        while (rs.next()) {
            String jk = rs.getString("jenis_kelamin").equals("L") ? "Laki-laki" : "Perempuan";

            model.addRow(new Object[]{
                rs.getInt("id"),
                rs.getString("nip"),
                rs.getString("nama_guru"),
                jk
            });
        }

        tableGuru.setModel(model);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal tampil data: " + e.getMessage());
    }
}

